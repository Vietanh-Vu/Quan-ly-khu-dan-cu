    private void btnLuuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLuuMouseClicked
        // TODO add your handling code here:
        if (tfSoHoKhau.getText().isEmpty() ||
                tfHoTen.getText().isEmpty() ||
                tfNgaySinh.getText().isEmpty() ||
                tfNguyenQuan.getText().isEmpty() ||
                tfNoiSinh.getText().isEmpty() ||
                tfDanToc.getText().isEmpty() ||
                tfQuanHeVoiChuHo.getText().isEmpty() ||
                tfNgayDangKyThuongTru.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đủ thông tin");          
        }
        else {
            try {
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/quan_ly_khu_dan_cu","root","");
                String sql = "UPDATE nhan_khau "
                    + "SET ho_ten = '" + tfHoTen.getText() + "',\n "
                    + "biet_danh = '" + tfBietDanh.getText() + "',\n "
                    + "gioi_tinh = '" + cbGioiTinh.getSelectedItem() + "',\n "
                    + "ngay_sinh = " + (tfNgaySinh.getText().isEmpty() ? "NULL" : ("'" + tfNgaySinh.getText() + "'")) + ",\n "
                    + "so_CMND = '" + tfSoCMND.getText() + "',\n "
                    + "ngay_cap_CMND = " + (tfNgayCap.getText().isEmpty() ? "NULL" : ("'" + tfNgayCap.getText() + "'")) + ",\n "
                    + "noi_cap_CMND = '" + tfNoiCap.getText() + "',\n "
                    + "noi_sinh = '" + tfNoiSinh.getText() + "',\n "
                    + "nguyen_quan = '" + tfNguyenQuan.getText() + "',\n "
                    + "dan_toc = '" + tfDanToc.getText() + "',\n "
                    + "nghe_nghiep = '" + tfNgheNghiep.getText() + "',\n "
                    + "noi_lam_viec = '" + tfNoiLamViec.getText() + "',\n "
                    + "quan_he_voi_chu_ho = '" + tfQuanHeVoiChuHo.getText() + "',\n "
                    + "ngay_dang_ky_thuong_tru = " + (tfNgayDangKyThuongTru.getText().isEmpty() ? "NULL" : ("'" + tfNgayDangKyThuongTru.getText() + "'")) + ",\n "
                    + "dia_chi_truoc_khi_chuyen = '" + tfDiaChiTruocKhiChuyen.getText() + "',\n "
                    + "chuyen_di = '" + cbChuyenDi.getSelectedItem() + "',\n "
                    + "so_ho_khau = '" + tfSoHoKhau.getText() + "',\n "
                    + "qua_doi = '" + cbQuaDoi.getSelectedItem() + "'\n "
                    + "WHERE nhan_khau_id = " + nhanKhau.getNhanKhauId() + ";";
                
                PreparedStatement add = conn.prepareStatement(sql);
                int row = add.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Đã lưu thành công");
                conn.close();
                this.dispose();
            }
            catch (Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Thông tin bạn điền có thể chưa chính xác.\n"
                        + "Có thể bạn đã nhập sai cú pháp ngày tháng phải là: yyyy-mm-dd.\n"
                        + "Hoặc là bạn đã điền sai thông tin của họ tên và số CMND của chủ hộ.\n");
            }
        }
    }//GEN-LAST:event_btnLuuMouseClicked

