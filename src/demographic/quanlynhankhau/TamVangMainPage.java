    // ------------- Save the data --------------------
    private void btnLuuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLuuMouseClicked
        // TODO add your handling code here:
        if (tfIdTamVang.getText().isEmpty() ||
                tfCMND.getText().isEmpty() ||
                tfTuNgay.getText().isEmpty() ||
                tfDenNgay.getText().isEmpty() ||
                tfNoiTamTru.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing information");
        }
        else {
            try {
                conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/demographic","root","");
                PreparedStatement add = conn.prepareStatement("INSERT INTO tam_vang VALUES (?,?,?,?,?) ");
                add.setString(1, tfCMND.getText());
                add.setString(2, tfIdTamVang.getText());
                add.setString(3, tfTuNgay.getText());
                add.setString(4, tfDenNgay.getText());
                add.setString(5, tfNoiTamTru.getText());
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Đã thêm." );
                conn.close();
                displayTamVang();
                clearAfterAdding();
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btnLuuMouseClicked

